export function getTranslation(lang: AppLanguage, key: string, params?: Record<string, string | number>): string {
  const langTranslations = t[lang] ?? t["en"];
  let text = langTranslations[key] ?? t["en"][key] ?? key;
  if (params) {
    Object.entries(params).forEach(([k, v]) => {
      text = text.replace(new RegExp(`\\{${k}\\}`, "g"), String(v));
    });
  }
  return text;
}
